# Create an empty list to store the results
results = []

# Loop from 3 to 81, incrementing by 3
for i in range(3, 81, 3):
    # Square the number
    squared = i ** 2
    # Divide the squared number by 3
    result = squared / 3
    # Append the result to the list
    results.append(result)

# Print the results
print(results)